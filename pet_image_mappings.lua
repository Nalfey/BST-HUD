-- Function to get the image file name for a pet
function get_pet_image(pet_name)
    -- Clean up the pet name (remove spaces and special characters)
    local clean_name = pet_name:gsub("%s+", ""):gsub("[^%w]", "")
    
    -- Try to find an exact match first
    if pet_image_mappings[pet_name] then
        return pet_image_mappings[pet_name]
    end
    
    -- Try to find a partial match
    for name, image in pairs(pet_image_mappings) do
        if name:lower():find(clean_name:lower(), 1, true) then
            return image
        end
    end
    
    -- Return default image if no match found
    return pet_image_mappings.default
end

return {
    get_pet_image = get_pet_image
} 
